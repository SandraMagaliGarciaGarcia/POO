import java.util.*;

class jmensaje {
    public static void main(String args[]) {
        System.out.println("for()");
        for (int a = 0; a < 10; a++) {
            System.out.println(a);
        }
        System.out.println("while");
        int a = 0;
        while (a < 10) {
            System.out.println(a);
            a++;
        }
        System.out.println("Do-while");
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < 10);

    }
}